Solamente los maestros pueden crear y editar libros. No existen planes para implementar el que los estudiantes puedan editar libros, pero alguien podría crear algo semejante para alumnos algún día (¿Portafolio?). La principal razón es mantener el módulo de libro tan básico como sea posible.

*¿Cómo busco los libros?*

Actualmente solo existe una manera, usando la capacidad de búsqueda de su navegador web en la página de impresión. La búsqueda global actualmente solo es posible en los foros de Moodle. Sería lindo que existiera una búsqueda global para todos los recursos, incluyendo libros. ¿Algún voluntario?

